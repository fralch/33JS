// Bitwise Operators en JavaScript

// Operador Bitwise AND
let resultado = 5 & 3; // Resultado: 1

// Operador Bitwise OR
resultado = 5 | 3; // Resultado: 7

// Operador Bitwise XOR
resultado = 5 ^ 3; // Resultado: 6

// Operador Bitwise NOT
resultado = ~5; // Resultado: -6

// Operador Bitwise Left Shift
resultado = 5 << 1; // Resultado: 10

// Operador Bitwise Right Shift
resultado = 5 >> 1; // Resultado: 2

// Operador Bitwise Zero Fill Right Shift
resultado = 5 >>> 1; // Resultado: 2

// Operador Bitwise AND Asignaci√≥n
resultado &= 3; // Resultado: 1
